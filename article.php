		<h3><span>七種錯誤飲食方式</span></h3>
		<p>☯導讀：雖然人們經常會把最新鮮、最有益健康的食品放入超市手推車，但很多人烹飪時，卻把我們真正需要的營養給白白扔掉了。為此，美國營養學家和食品安全專家，列舉了聰明主婦在烹飪方面也會常犯的7種錯誤，聽從他們的建議，你餐桌上的食物會變得更加健康。</p>
		<br>
		<p>✿錯誤一：快炒大蒜。美國癌症研究協會營養學研究小組組長約翰•米爾納博士說：“把大蒜拍碎、切片，放上至少10分鐘後再烹飪。拍碎大蒜會引發一種酶的化學反應，釋放出一種能抗癌的化合物。放上10分鐘再烹飪⋯⋯，就是要讓這種化合物有充足的時間全部形成。”</p>
		<br>
		<p>✿錯誤二：把食物貯藏在透明容器中。如果你現在喝的牛奶還是透明塑膠袋裝奶，建議你考慮換成硬紙盒包裝的牛奶。比利時根特大學的研究人員指出，牛奶富含的核黃素暴露在日光下易發生損失。美國食品科學家為此建議，儘量避免將牛奶和穀物放在透明容器裡，可以保留營養。</p>
		<br>
		<p>✿錯誤三：一次採購過多蔬菜。紐約聖基斯科北方韋斯特賈斯特醫院健康顧問格裡•布魯斯特博士說：“從採摘的那一刻起，水果和蔬菜中的維生素和礦物質就開始減少了。”也就是說，你採購回來的蔬菜存儲時間越久，它們所含的營養就越少。研究發現，在冰箱保存大約一周後，菠菜中有一半的葉酸和40%的葉黃素會自然流失。格裡博士建議每次採購食物不要過多，一周買三次最為合適。</p>
		<br>
		<p>✿錯誤四：少用調料。在不增加食用油和食鹽的情況下多用一些植物調料和香料，不僅能起到調味的作用，還能保護人們免受食物中毒之害。香港大學的研究人員在對20種常見調料所做的抗菌(包括大腸桿菌、葡萄球菌和沙門氏菌等)試驗發現，丁香、肉桂都具有很強的抗菌能力。發表在美國《農 業和食品化學雜誌》上的另外一項研究成果稱，迷迭香、百里香、肉豆蔻和月桂樹葉也富含抗氧化劑。每次烹飪多加半匙調料，既安全又健康。</p>
		<br>
		<p>✿錯誤五：給水果多剝幾層皮。發表在美國《營養研究雜誌》上的研究發現，多數水果表皮中進行的抗氧化活動比水果果肉中進行的要高出2—27倍。營養學家建議，土豆和胡蘿蔔的皮只要輕輕刮掉一層就夠了。如果有些果蔬必須剝皮才能入口，去掉盡可能薄的一層皮就可以。
		</p>
		<p>✿錯誤六：把維生素和礦物質慢慢燉掉。一般人認為，煮食是一種簡單的烹飪方法，不用加油，又不損失營養。但實際上，這 種烹飪方法最多可導致90%的營養成分流失。美國癌症研究協會營養顧問卡倫•科林斯博士說，“鉀和可溶*的維生素等混入水中就會溢出。”加入少量的水燉 煮，用電磁爐慢煮或爆炒，可減少營養成分的流失。爆炒則適用於深綠色或橙色的蔬菜。</p>
		<br>
		<p>✿錯誤七：有些食物該洗不洗。吃李子或草莓的時候，我們都會先清洗一下。但吃香蕉、桔子或芒果的時候，很少有人把它們 放進水沖洗。清洗剝皮吃的食物聽起來有些奇怪，但停留在食物表面的有害細菌可能會沾染到手上，切開水果時甚至會侵入水果內部。手拿剝皮水果前用肥皂或溫水 洗手至少20秒，可有效防止細菌蔓延。</p>
